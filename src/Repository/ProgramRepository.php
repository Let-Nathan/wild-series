    public function findLikeName(?string $name)

    {
        $queryBuilder = $this->createQueryBuilder('p')

            ->where('p.title LIKE :name')->orWhere('a.firstname LIKE :name')
            ->leftJoin('p.actors', 'a')
            ->setParameter('name', '%' . $name . '%')
            ->orderBy('p.title', 'ASC')
            ->getQuery();

        return $queryBuilder->getResult();
    }

